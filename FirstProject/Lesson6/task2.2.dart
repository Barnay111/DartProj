void main() {
  final int myAge = 19;
  final bool isOlderThan18 = myAge > 18;
  final bool isEqualTo25 = myAge == 25;
  final bool isNotEqualTo16 = myAge != 16;

  print("Am I older than 18?: $isOlderThan18");
  print("Am I equal to 25?: $isEqualTo25");
  print("Am I not equal to 16?: $isNotEqualTo16");
}